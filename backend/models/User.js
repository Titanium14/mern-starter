const mongoose = require('mongoose');
const moment = require('moment');
const Schema = mongoose.Schema;

// Create Schema
const UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: moment()
  }
});

module.exports = User = mongoose.model('users', UserSchema);
